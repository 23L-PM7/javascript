// Use getElementById to select an element by ID
const myDiv = document.getElementById("myDiv");
console.log(myDiv);

// Use getElementsByName to select elements by name
const usernameElements = document.getElementsByName("username");
console.log(usernameElements);

// Use getElementsByTagName to select elements by tag name
const listItems = document.getElementsByTagName("li");
console.log(listItems);

// Use getElementsByClassName to select elements by class name
const itemsWithClass = document.getElementsByClassName("item");
console.log(itemsWithClass);
